uint32_t rotl(uint32_t x, int n) { return (x << n) | (x >> (32 - n)); } // 32位循环左移n位，高位回到低位

uint32_t tau(uint32_t A)  //4个字节分别查S盒
{
    uint32_t B = 0;                                                    // 保存S盒替换后的32位结果
    B |= Sbox[(A >> 24) & 0xff] << 24;                                // 取最高8位→查S盒→放回最高8位
    B |= Sbox[(A >> 16) & 0xff] << 16;                                // 取次高8位→查S盒→放回次高8位
    B |= Sbox[(A >> 8) & 0xff] << 8;                                  // 取第三个字节→查S盒→放回第三个字节
    B |= Sbox[A & 0xff];                                              // 取最低8位→查S盒→放回最低8位
    return B;                                                         // 返回S盒替换后的32位数据
}

uint32_t L(uint32_t B) { return B ^ rotl(B, 2) ^ rotl(B, 10) ^ rotl(B, 18) ^ rotl(B, 24); } // 加密线性扩散L
uint32_t L_key(uint32_t B) { return B ^ rotl(B, 13) ^ rotl(B, 23); }                        // 密钥扩展线性扩散L'
uint32_t T(uint32_t A) { return L(tau(A)); }                                                // 加密轮函数T：S盒+线性扩散
uint32_t T_key(uint32_t A) { return L_key(tau(A)); }                                        // 密钥扩展轮函数T'

void generate_round_keys(const uint32_t MK[4], uint32_t rk[32])
{
    uint32_t K[36];                                                   // 中间状态数组：K0~K35
    for (int i = 0; i < 4; i++) K[i] = MK[i] ^ FK[i];                 // 主密钥MK与系统参数FK异或，得到K0~K3
    for (int i = 0; i < 32; i++) {                                    // 递推生成32个轮密钥
        K[i + 4] = K[i] ^ T_key(K[i + 1] ^ K[i + 2] ^ K[i + 3] ^ CK[i]); // 生成下一轮状态
        rk[i] = K[i + 4];                                             // 当前状态作为第i轮轮密钥
    }
}

void sm4_encrypt(const uint32_t in[4], uint32_t out[4], const uint32_t rk[32])
{
    uint32_t X[36];                                                   // 加密状态数组：X0~X35
    for (int i = 0; i < 4; i++) X[i] = in[i];                         // 把128bit明文放入X0~X3
    for (int i = 0; i < 32; i++) X[i + 4] = X[i] ^ T(X[i + 1] ^ X[i + 2] ^ X[i + 3] ^ rk[i]); // 第i轮加密
    out[0] = X[35]; out[1] = X[34]; out[2] = X[33]; out[3] = X[32];    // 最后反序输出密文
}

void sm4_decrypt(const uint32_t in[4], uint32_t out[4], const uint32_t rk[32])
{
    uint32_t drk[32];                                                  // 解密轮密钥
    for (int i = 0; i < 32; i++) drk[i] = rk[31 - i];                  // 解密时轮密钥顺序反过来
    sm4_encrypt(in, out, drk);                                         // SM4解密结构和加密结构相同
}

void print_block(const char* name, const uint32_t block[4])
{
    cout << name << ": ";                                              // 输出数据名称
    for (int i = 0; i < 4; i++) cout << hex << setw(8) << setfill('0') << block[i]; // 按8位十六进制输出
    cout << dec << endl;                                               // 恢复十进制输出
}

int main()
{
    uint32_t key[4] = { 0x0123456a,0x89abcdef,0xfedcba98,0x76543210 };    // 128bit主密钥，可自行修改
    uint32_t plain[4] = { 0x01234567,0x89abcdef,0xfedcba98,0x76543210 };  // 128bit明文
    uint32_t cipher[4];                                                // 存放加密后的密文
    uint32_t decrypt[4];                                               // 存放解密后的明文
    uint32_t rk[32];                                                   // 存放32个轮密钥
    generate_round_keys(key, rk);                                      // 根据主密钥生成32轮轮密钥
    sm4_encrypt(plain, cipher, rk);                                    // 执行SM4加密
    sm4_decrypt(cipher, decrypt, rk);                                  // 执行SM4解密
    print_block("Plain ", plain);                                      // 输出原始明文
    print_block("Cipher", cipher);                                     // 输出加密密文
    print_block("Decrypt", decrypt);                                   // 输出解密结果
    return 0;                                                          // 程序正常结束
}